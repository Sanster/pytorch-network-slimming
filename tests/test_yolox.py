def test_YOLOXHead():
    in_channels = [256, 512, 1024]
    model = YOLOXHead(80, width=1, in_channels=in_channels)
    x = [torch.Tensor(1, in_channel, 224, 224) for in_channel in in_channels]

    config = gen_pruning_schema(model, x)

    with tempfile.NamedTemporaryFile("w") as f:
        json.dump(config, f, indent=2, ensure_ascii=False)
        f.flush()
        pruner = SlimPruner(model, f.name)
        pruner.run(0.6)
        pruner.pruned_model.eval()
        x = [torch.Tensor(1, in_channel, 224, 224) for in_channel in in_channels]
        y = pruner.pruned_model(x)
        print(y)


